package EF_ClaudiaZeta_MicroservicioTurista.dto;

public class TuristaDTO {

	public Integer codTurista;
	public String nombreturista;
	public String celular;
	public String direccion;
	
	
	public Integer getCodTurista() {
		return codTurista;
	}
	public void setCodTurista(Integer codTurista) {
		this.codTurista = codTurista;
	}
	public String getNombreturista() {
		return nombreturista;
	}
	public void setNombreturista(String nombreturista) {
		this.nombreturista = nombreturista;
	}
	public String getCelular() {
		return celular;
	}
	public void setCelular(String celular) {
		this.celular = celular;
	}
	public String getDireccion() {
		return direccion;
	}
	public void setDireccion(String direccion) {
		this.direccion = direccion;
	}
	
	
	
	
}
